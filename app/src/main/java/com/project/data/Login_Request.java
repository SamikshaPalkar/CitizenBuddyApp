package com.project.data;

public class Login_Request
{
	public static String User_Id;
	public static String Username;
	public static String PhoneNumber;
	public static String Password;
	
	public static String Complaint_Id;
	public static String Status;
	public static String Reply;
	
	
	public static String GetUser_Id()
	{
		return User_Id;
	}
	public static void SetUser_Id(String RUser_Id)
	{
		User_Id=RUser_Id;
	}
	
	public static String GetUsername()
	{
		return Username;
	}
	public static void SetUsername(String RUsername)
	{
		Username=RUsername;
	}
	
	public static String GetPhoneNumber()
	{
		return PhoneNumber;
	}
	public static void SetPhoneNumber(String RPhoneNumber)
	{
		PhoneNumber=RPhoneNumber;
	}
	
	public static String GetPassword()
	{
		return Password;
	}
	public static void SetPassword(String RPassword)
	{
		Password=RPassword;
	}
	
	public static String GetComplaint_Id()
	{
		return Complaint_Id;
	}
	public static void SetComplaint_Id(String RComplaint_Id)
	{
		Complaint_Id=RComplaint_Id;
	}
	
	public static String GetStatus()
	{
		return Status;
	}
	public static void SetStatus(String RStatus)
	{
		Status=RStatus;
	}
	
	public static String GetReply()
	{
		return Reply;
	}
	public static void SetReply(String RReply)
	{
		Reply=RReply;
	}
	
	
}
